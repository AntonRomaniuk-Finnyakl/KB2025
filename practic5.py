import math

# 1. Формула руху з прискоренням
s0 = 0
v0 = 10
t = 5
g = 9.8
s = s0 + v0 * t + 0.5 * g * t**2
print("s =", s)

# 2. Формула майбутньої вартості
PV = 1000
INT = 5
YRS = 10
FV = PV * (1 + INT / 100) ** YRS
print("FV =", FV)

# 3. Формула гравітаційної сталої для двох мас
a = 10
p = 20
m1 = 5.97e24  # маса Землі (наприклад)
m2 = 7.35e22  # маса Місяця (наприклад)
G = (4 * math.pi**2 * a**3) / (p**2 * (m1 + m2))
print("G =", G)

# 4. Теорема косинусів
a = 7
b = 5
gamma = math.radians(60)  # кут у градусах - у радіани
c = math.sqrt(a**2 + b**2 - 2 * a * b * math.cos(gamma))
print("c =", c)
